"use client";

import { useState } from "react";
import { useRouter } from "next/navigation";
import Link from "next/link";
import { getSession } from "next-auth/react";

export default function RegisterPage() {
  const [name, setName] = useState("");
  const [email, setEmail] = useState("");
  const [password, setPassword] = useState("");
  const [error, setError] = useState("");
  const router = useRouter();

  async function handleSubmit(e: React.FormEvent) {
    e.preventDefault();
    setError("");

    try {
      const res = await fetch("/api/auth/signup", {
        method: "POST",
        headers: { "Content-Type": "application/json" },
        body: JSON.stringify({ name, email, password }),
      });

      const data = await res.json();

      if (!res.ok) {
        throw new Error(data.error || "Failed to register");
      }
      router.push(`/boards`);
    } catch (err: unknown) {
      const error = err as Error;
      setError(error.message);
    }
  }

  return (
    <div className="flex min-h-screen items-center justify-center bg-gray-50">
      <form
        onSubmit={handleSubmit}
        className="bg-white p-15 shadow-md rounded-lg w-full max-w-lg border-4 border-orange-400"
      >
        <h1 className="text-4xl font-bold mb-6 text-center text-orange-600">
          Welcome to Taskflow!
        </h1>
        <h1 className="text-xl font-bold mb-6 text-center">
          Kindly Register to Start Your Journey
        </h1>

        {error && <p className="text-orange-600 mb-4">{error}</p>}

        <input
          type="text"
          placeholder="Full Name"
          value={name}
          onChange={(e) => setName(e.target.value)}
          className="w-full mb-4 px-4 py-2 border border-gray-400 rounded-lg"
        />

        <input
          type="email"
          placeholder="Email"
          value={email}
          onChange={(e) => setEmail(e.target.value)}
          className="w-full mb-4 px-4 py-2 border border-gray-400 rounded-lg"
        />

        <input
          type="password"
          placeholder="Password"
          value={password}
          onChange={(e) => setPassword(e.target.value)}
          className="w-full mb-4 px-4 py-2 border border-gray-400 rounded-lg"
        />
        <div className="w-full flex flex-col justify-center items-center space-y-4">
          <button
            type="submit"
            className="px-6 py-3 cursor-pointer rounded-full w-full bg-linear-to-r from-orange-500 to-orange-700 text-white  hover:bg-linear-to-r hover:from-orange-600 hover:to-orange-800 transition duration-300"
          >
            Register
          </button>
          <Link
            href="/"
            className=" px-6 py-3 text-center cursor-pointer rounded-full w-full  text-black border hover:bg-gray-200 duration-500"
          >
            Go Back
          </Link>
        </div>
      </form>
    </div>
  );
}
